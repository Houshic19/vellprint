async function getBlogPosts(category) {
  if (DB_TYPE === 'oracle') {
    const conn = await pool.getConnection();
    try {
      let sql = 'SELECT * FROM blog_posts ORDER BY created_at DESC';
      let binds = {};
      if (category) {
        sql = 'SELECT * FROM blog_posts WHERE category = :category ORDER BY created_at DESC';
        binds = { category };
      }
      const result = await conn.execute(sql, binds);
      return result.rows;
    } finally { await conn.close(); }
  } else {
    const data = readMockData();
    let posts = (data.blog_posts || []).slice().reverse();
    if (category) {
      posts = posts.filter(p => p.category === category);
    }
    return posts;
  }
}

async function getBlogPostBySlug(slug) {
  if (DB_TYPE === 'oracle') {
    const conn = await pool.getConnection();
    try {
      const result = await conn.execute('SELECT * FROM blog_posts WHERE slug = :slug', { slug });
      return result.rows.length > 0 ? result.rows[0] : null;
    } finally { await conn.close(); }
  } else {
    const data = readMockData();
    return (data.blog_posts || []).find(p => p.slug === slug) || null;
  }
}

async function addBlogPost(post) {
  if (DB_TYPE === 'oracle') {
    const conn = await pool.getConnection();
    try {
      const result = await conn.execute(
        `INSERT INTO blog_posts (title, slug, category, excerpt, content, image_path, author)
         VALUES (:title, :slug, :category, :excerpt, :content, :image_path, :author)
         RETURNING id INTO :id`,
        { ...post, id: { type: oracledb.NUMBER, dir: oracledb.BIND_OUT } }
      );
      await conn.commit();
      return result.outBinds.id[0];
    } finally { await conn.close(); }
  } else {
    const data = readMockData();
    if (!data.blog_posts) data.blog_posts = [];
    const newId = data.blog_posts.length > 0 ? Math.max(...data.blog_posts.map(p => p.id)) + 1 : 1;
    const newPost = { id: newId, ...post, created_at: new Date().toISOString() };
    data.blog_posts.push(newPost);
    writeMockData(data);
    return newId;
  }
}

async function deleteBlogPost(id) {
  if (DB_TYPE === 'oracle') {
    const conn = await pool.getConnection();
    try {
      await conn.execute('DELETE FROM blog_posts WHERE id = :id', { id });
      await conn.commit();
    } finally { await conn.close(); }
  } else {
    const data = readMockData();
    data.blog_posts = (data.blog_posts || []).filter(p => p.id !== id);
    writeMockData(data);
  }
}

