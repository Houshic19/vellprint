async function getProducts(params = {}) {
  if (DB_TYPE === 'oracle') {
    const conn = await pool.getConnection();
    try {
      let query = `
        SELECT p.id, p.name, p.brand_id, b.name as brand_name, p.part_number, p.oem_part_number, 
               p.alternate_part_number, p.hsn_code, p.sku, p.category_id, c.name as category_name,
               p.short_description, p.warranty, p.moq, p.unit, p.weight, p.availability, 
               p.stock_status, p.image_path, p.datasheet_path, p.is_featured, p.is_popular, 
               p.is_new_arrival, p.meta_title, p.meta_description, p.seo_url
        FROM products p
        LEFT JOIN brands b ON p.brand_id = b.id
        LEFT JOIN categories c ON p.category_id = c.id
        WHERE 1=1
      `;
      const binds = {};

      if (params.search) {
        query += ` AND (LOWER(p.name) LIKE :search 
                     OR LOWER(p.part_number) LIKE :search 
                     OR LOWER(p.oem_part_number) LIKE :search 
                     OR LOWER(p.alternate_part_number) LIKE :search 
                     OR LOWER(p.sku) LIKE :search)`;
        binds.search = '%' + params.search.toLowerCase() + '%';
      }

      if (params.brand_id) {
        query += ` AND p.brand_id = :brand_id`;
        binds.brand_id = parseInt(params.brand_id);
      }

      if (params.category_id) {
        query += ` AND p.category_id = :category_id`;
        binds.category_id = parseInt(params.category_id);
      }

      if (params.is_featured) {
        query += ` AND p.is_featured = 1`;
      }
      if (params.is_popular) {
        query += ` AND p.is_popular = 1`;
      }
      if (params.is_new_arrival) {
        query += ` AND p.is_new_arrival = 1`;
      }

      // Sort
      if (params.sort === 'newest') {
        query += ` ORDER BY p.id DESC`;
      } else if (params.sort === 'az') {
        query += ` ORDER BY p.name ASC`;
      } else if (params.sort === 'za') {
        query += ` ORDER BY p.name DESC`;
      } else {
        query += ` ORDER BY p.id ASC`;
      }

      const result = await conn.execute(query, binds);
      
      // Load compatibilities for products
      const products = result.rows.map(row => ({
        id: row.ID,
        name: row.NAME,
        brand_id: row.BRAND_ID,
        brand_name: row.BRAND_NAME,
        part_number: row.PART_NUMBER,
        oem_part_number: row.OEM_PART_NUMBER,
        alternate_part_number: row.ALTERNATE_PART_NUMBER,
        hsn_code: row.HSN_CODE,
        sku: row.SKU,
        category_id: row.CATEGORY_ID,
        category_name: row.CATEGORY_NAME,
        short_description: row.SHORT_DESCRIPTION,
        warranty: row.WARRANTY,
        moq: row.MOQ,
        unit: row.UNIT,
        weight: row.WEIGHT,
        availability: row.AVAILABILITY,
        stock_status: row.STOCK_STATUS,
        image_path: row.IMAGE_PATH,
        datasheet_path: row.DATASHEET_PATH,
        is_featured: row.IS_FEATURED,
        is_popular: row.IS_POPULAR,
        is_new_arrival: row.IS_NEW_ARRIVAL,
        meta_title: row.META_TITLE,
        meta_description: row.META_DESCRIPTION,
        seo_url: row.SEO_URL,
        compatibilities: []
      }));

      return products;
    } finally {
      await conn.close();
    }
  } else {
    // Local JSON mock filtering
    const data = readMockData();
    let products = data.products || [];

    if (params.search) {
      const q = params.search.toLowerCase();
      products = products.filter(p => 
        p.name.toLowerCase().includes(q) || 
        (p.part_number && p.part_number.toLowerCase().includes(q)) ||
        (p.oem_part_number && p.oem_part_number.toLowerCase().includes(q)) ||
        (p.alternate_part_number && p.alternate_part_number.toLowerCase().includes(q)) ||
        (p.sku && p.sku.toLowerCase().includes(q))
      );
    }

    if (params.brand_id) {
      products = products.filter(p => p.brand_id === parseInt(params.brand_id));
    }

    if (params.category_id) {
      products = products.filter(p => p.category_id === parseInt(params.category_id));
    }

    if (params.is_featured) {
      products = products.filter(p => p.is_featured === 1);
    }
    if (params.is_popular) {
      products = products.filter(p => p.is_popular === 1);
    }
    if (params.is_new_arrival) {
      products = products.filter(p => p.is_new_arrival === 1);
    }

    // Sort
    if (params.sort === 'newest') {
      products.sort((a, b) => b.id - a.id);
    } else if (params.sort === 'az') {
      products.sort((a, b) => a.name.localeCompare(b.name));
    } else if (params.sort === 'za') {
      products.sort((a, b) => b.name.localeCompare(a.name));
    }

    // Map brand and category names
    return products.map(p => {
      const brand = data.brands.find(b => b.id === p.brand_id);
      const cat = data.categories.find(c => c.id === p.category_id);
      const compatibilities = (data.product_compatibilities || []).filter(c => c.product_id === p.id);
      return {
        ...p,
        brand_name: brand ? brand.name : 'Unknown',
        category_name: cat ? cat.name : 'Unknown',
        compatibilities
      };
    });
  }
}

