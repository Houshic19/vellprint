async function initDB() {
  if (DB_TYPE === 'oracle') {
    try {
      console.log('Initializing Oracle Database connection pool...');
      pool = await oracledb.createPool({
        user: process.env.DB_USER,
        password: process.env.DB_PASSWORD,
        connectString: process.env.DB_CONNECT_STRING,
        poolMin: 1,
        poolMax: 10,
        poolIncrement: 1
      });
      console.log('Oracle DB connection pool established.');

      const conn = await pool.getConnection();
      try {
        const result = await conn.execute(
          `SELECT table_name FROM user_tables WHERE table_name IN (
            'ADMIN_USERS', 'OFFERS', 'GALLERY', 'BRANDS', 'CATEGORIES', 
            'PRODUCTS', 'PRODUCT_COMPATIBILITIES', 'DEALER_REGISTRATIONS', 'CART_ENQUIRIES'
          )`
        );
        
        const existingTables = result.rows.map(row => row.TABLE_NAME);

        // Core admin tables
        if (!existingTables.includes('ADMIN_USERS')) {
          console.log('Creating table ADMIN_USERS...');
          await conn.execute(schemaSqls[0]);
          const adminPass = process.env.ADMIN_PASSWORD || 'admin123';
          const adminUser = process.env.ADMIN_USERNAME || 'admin';
          const hash = await bcrypt.hash(adminPass, 10);
          await conn.execute(
            `INSERT INTO admin_users (username, password_hash) VALUES (:username, :hash)`,
            { username: adminUser, hash: hash }
          );
          await conn.commit();
        }

        if (!existingTables.includes('OFFERS')) {
          console.log('Creating table OFFERS...');
          await conn.execute(schemaSqls[1]);
          await conn.commit();
        }

        if (!existingTables.includes('GALLERY')) {
          console.log('Creating table GALLERY...');
          await conn.execute(schemaSqls[2]);
          await conn.commit();
        }

        // B2B Marketplace Tables
        if (!existingTables.includes('BRANDS')) {
          console.log('Creating table BRANDS...');
          await conn.execute(schemaSqls[3]);
          await conn.commit();
        }

        if (!existingTables.includes('CATEGORIES')) {
          console.log('Creating table CATEGORIES...');
          await conn.execute(schemaSqls[4]);
          await conn.commit();
        }

        if (!existingTables.includes('PRODUCTS')) {
          console.log('Creating table PRODUCTS...');
          await conn.execute(schemaSqls[5]);
          await conn.commit();
        }

        if (!existingTables.includes('PRODUCT_COMPATIBILITIES')) {
          console.log('Creating table PRODUCT_COMPATIBILITIES...');
          await conn.execute(schemaSqls[6]);
          await conn.commit();
        }

        if (!existingTables.includes('DEALER_REGISTRATIONS')) {
          console.log('Creating table DEALER_REGISTRATIONS...');
          await conn.execute(schemaSqls[7]);
          await conn.commit();
        }

        if (!existingTables.includes('CART_ENQUIRIES')) {
          console.log('Creating table CART_ENQUIRIES...');
          await conn.execute(schemaSqls[8]);
          await conn.commit();
        }

      } finally {
        await conn.close();
      }

    } catch (err) {
      console.error('Error initializing Oracle DB. Falling back to local Mock JSON DB.', err);
      process.env.DB_TYPE = 'mock'; // fallback
      initMockDB();
    }
  } else {
    initMockDB();
  }
}

